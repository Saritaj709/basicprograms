package com.bridgelabz.model;

public class Stock {
private String stockName;
private String stockSymbol;
private int noOfStock;
private Double stockCharge;
public String getStockName() {
	return stockName;
}
public void setStockName(String stockName) {
	this.stockName = stockName;
}
public String getStockSymbol() {
	return stockSymbol;
}
public void setStockSymbol(String stockSymbol) {
	this.stockSymbol = stockSymbol;
}
public int getNoOfStock() {
	return noOfStock;
}
public void setNoOfStock(int noOfStock) {
	this.noOfStock = noOfStock;
}
public Double getStockCharge() {
	return stockCharge;
}
public void setStockCharge(Double double1) {
	this.stockCharge = double1;
}
}
